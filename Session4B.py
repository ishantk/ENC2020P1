# Assignment Operators =, +=, -=, *=, **=, /=, //=, %=

# = operator will be helping us to create a container in memory
n1 = 10
# n1 = n1 + 20
n1 += 20
print(">> n1 is:", n1)

n1 //= 2
print(">> n1 is:", n1)

n1 %= 3     # n1 = n1 % 3
print(">> n1 is:", n1)  # ?

